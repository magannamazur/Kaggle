# Integers
x = 14
print(x)
print(type(x))

# Floats
nearly_pi = 3.141592653589793238462643383279502884197169399375105820974944
print(nearly_pi)
print(type(nearly_pi))

# Round to 5 decimal places
rounded_pi = round(nearly_pi, 5)
print(rounded_pi)
print(type(rounded_pi))

# Booleans
z_one = True
print(z_one)
print(type(z_one))

# Value of expression
z_three = (1 < 2)
print(z_three)
print(type(z_three))

# Strings
w = "Hello, Python!"
print(w)
print(type(w))

# Length
print(len(w))

# String into float
my_number = "1.12321"
print(my_number)
print(type(my_number))


also_my_number = float(my_number)
print(also_my_number)
print(type(also_my_number))

# concatenating
new_string = "abc" + "def"
print(new_string)
print(type(new_string))

# multiplying
newest_string = "abc" * 3
print(newest_string)
print(type(newest_string))
